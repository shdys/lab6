package model;

public class Participant {
	public String userName;
	public String userEmail;
	public String userSex;
	public String leaveEarly;
	public String payMorePercentage;
	public String moneyShouldPay;
	public String Sum;
	public String Type;
	public String IsCreater;
	
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserSex() {
		return userSex;
	}
	public void setUserSex(String userSex) {
		this.userSex = userSex;
	}
	public String getLeaveEarly() {
		return leaveEarly;
	}
	public void setLeaveEarly(String leaveEarly) {
		this.leaveEarly = leaveEarly;
	}
	public String getPayMorePercentage() {
		return payMorePercentage;
	}
	public void setPayMorePercentage(String payMorePercentage) {
		this.payMorePercentage = payMorePercentage;
	}
	public String getMoneyShouldPay() {
		return moneyShouldPay;
	}
	public void setMoneyShouldPay(String moneyShouldPay) {
		this.moneyShouldPay = moneyShouldPay;
	}
	public String getSum() {
		return Sum;
	}
	public void setSum(String sum) {
		this.Sum = sum;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		this.Type = type;
	}
	public String getIsCreater() {
		return IsCreater;
	}
	public void setIsCreater(String isCreater) {
		this.IsCreater = isCreater;
	}
}
